package com.viit.tnp.tpo;

public class TPO {

    String designation, joiningDate, username;
    int tpoId, personId, departmentId, salary;

    public TPO(String designation, String joiningDate, int departmentId, int salary) {
        this.designation = designation;
        this.joiningDate = joiningDate;
        this.departmentId = departmentId;
        this.salary = salary;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getDesignation() {
        return designation;
    }

    public void setDesignation(String designation) {
        this.designation = designation;
    }

    public String getJoiningDate() {
        return joiningDate;
    }

    public void setJoiningDate(String joiningDate) {
        this.joiningDate = joiningDate;
    }

    public int getTpoId() {
        return tpoId;
    }

    public void setTpoId(int tpoId) {
        this.tpoId = tpoId;
    }

    public int getPersonId() {
        return personId;
    }

    public void setPersonId(int personId) {
        this.personId = personId;
    }

    public int getDepartmentId() {
        return departmentId;
    }

    public void setDepartmentId(int departmentId) {
        this.departmentId = departmentId;
    }

    public int getSalary() {
        return salary;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }

}
